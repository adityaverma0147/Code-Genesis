if __name__ == '__main__':
    import os
    code = """
int factorial(int n) {
    if (n <= 1) {
        return 1;
    }
    return n * factorial(n - 1);
}

int main() {
    int x = 5;
    int result = factorial(x);
    printf("Factorial is");
    printf(result);
    return 0;
}
"""
    lexer  = Lexer(code)
    tokens = lexer.tokenize()
    parser = Parser(tokens)
    ast    = parser.parse()

    codegen = CodeGenerator()
    python_code = codegen.generate(ast)

    print("\n" + "="*55)
    print("PHASE 6: GENERATED PYTHON CODE")
    print("="*55)
    print(python_code)
    print("="*55)

    # Optionally save to file
    out_path = os.path.join(os.path.dirname(__file__), 'output.py')
    with open(out_path, 'w') as f:
        f.write(python_code)
    print(f"\n[✓] Python code saved to: {out_path}")
